import type { WeaponPreset, ArmorPreset } from '../types';

export function buildShopSystemPrompt(weapons: WeaponPreset[], armors: ArmorPreset[], purchasedWeaponIds: Set<string>, purchasedArmorIds: Set<string>): string {
  const weaponList = weapons.map((w) => {
    const typeLabel = w.weaponType === 'melee' ? '近战' : '远程';
    const soldTag = purchasedWeaponIds.has(w.id) ? '（已卖完）' : '';
    return `- [ID: ${w.id}] ${w.name}（${typeLabel}）: 伤害 ${w.damage}, 持久度 ${w.durability}, 价格 ${w.price} 金币${w.effect ? `, 特效: ${w.effect}` : ''}. 描述: ${w.description}${soldTag}`;
  }).join('\n');

  const armorList = armors.map((a) => {
    const typeLabel = a.armorType === 'helmet' ? '头盔' : a.armorType === 'chest' ? '护甲' : '盾牌';
    const soldTag = purchasedArmorIds.has(a.id) ? '（已卖完）' : '';
    let stats = '';
    if (a.armorType === 'helmet') {
      stats = `伤害减免 ${Math.round((a.damageReduction ?? 0) * 100)}%, 持久度 ${a.durability}`;
    } else if (a.armorType === 'chest') {
      stats = `生命 +${a.bonusHp ?? 0}`;
    } else {
      stats = `防御 ${a.defense ?? 0}, 持久度 ${a.durability}`;
    }
    return `- [ID: ${a.id}] ${a.name}（${typeLabel}）: ${stats}, 价格 ${a.price} 金币${a.effect ? `, 特效: ${a.effect}` : ''}. 描述: ${a.description}${soldTag}`;
  }).join('\n');

  const equipmentSection = [weaponList, armorList].filter(Boolean).join('\n');

  return `${DM_SHOP_PROMPT}\n\n【货架装备】\n${equipmentSection}\n\n请从以上装备中向玩家推荐，不要推荐列表外的装备。标注"（已卖完）"的装备已经售罄（用户已经购买过了），不要再推荐，也无法再购买。\n- 用户购买成功后，系统会处理购买并从用户金币中扣除总价，你只需口头回应（如"好眼光！"、"祝你好运！"）。\n- 如果系统告知金币不足（或其他原因无法购买），你应礼貌地告诉用户金币不够（无法购买的原因），需要继续攒钱或购买其他装备。\n\n【购买通知】\n当系统发送" （系统：已成功购买 XXX，共花费 XXX 金币，剩余 XXX 金币）"，表示用户已经完成了购买，已经将该装备加入了其背包。你只需口头回复感谢，不要在 JSON 中重复价格或数量，此时该装备在【货架装备】中应该是已卖完状态。你可以向冒险者再介绍其他装备。`;
}
