import type { WeaponPreset } from '../types';

export function buildShopSystemPrompt(weapons: WeaponPreset[]): string {
  const weaponList = weapons.map((w) => {
    const typeLabel = w.weaponType === 'melee' ? '近战' : '远程';
    return `- ${w.name}（${typeLabel}）: 伤害 ${w.damage}, 持久度 ${w.durability}, 价格 ${w.price} 金币${w.effect ? `, 特效: ${w.effect}` : ''}. 描述: ${w.description}`;
  }).join('\n');

  return `${DM_SHOP_PROMPT}\n\n【货架装备】\n${weaponList}\n\n请从以上装备中向玩家推荐，不要推荐列表外的装备。\n\n【购买通知】\n当用户发送"（我购买了 XXX，共花费 XXX 金币）"时，表示用户刚刚通过前端界面完成了购买，已经将该装备加入了其背包。你只需口头回应（如"好眼光！"、"祝你好运！"），不要在 JSON 中重复价格或数量。`;
}
